    private void darBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_darBajaActionPerformed
        // TODO add your handling code here:
        Jugador aux = manager.getJugadorBuscado();
        int confirmacion = JOptionPane.showConfirmDialog(this, "El jugador " + aux.getNombre() + " " + aux.getPrimerApellido() + " " + aux.getSegundoApellido() + " sera dado de baja de la plantilla", "Confirmar baja", JOptionPane.YES_NO_CANCEL_OPTION);
        if (confirmacion == JOptionPane.YES_OPTION) {
            manager.darBajaJugador(manager.recuperarJugador());
            manager.guardarDatosJugador();
            JOptionPane.showMessageDialog(this, "El jugado ha sido dado de baja", "Aviso", JOptionPane.WARNING_MESSAGE);
            MostrarPlantilla vistaPlantilla = new MostrarPlantilla();
            vistaPlantilla.setVisible(true);
            dispose();
        } else if (confirmacion == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(this, "Has actuado de forma correcta , el jugador se merece una segunda oportunidad", "Segunda oportunidad", JOptionPane.WARNING_MESSAGE);
        } else if (confirmacion == JOptionPane.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(this, "Intenta no equivocarte de boton la proxima vez", "Vaya fallo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_darBajaActionPerformed

